<template>
	<div
		class="skeleton__container"
		:style="computedStyle"
	/>
</template>

<script>
export default {
	props: {
		width: {
			type: String,
			required: false,
			default: '20',
		},

		height: {
			type: String,
			required: false,
			default: '16',
		},
	},

	computed: {
		computedStyle() {
			return  {
				'--width': `${this.width}px`,
				'--height': `${this.height}px`,
			};
		},
	},
}
</script>

<style lang="scss" scoped>
	@import '../assets/sass/tokens.scss';

	.skeleton__container {
		position: relative;
		overflow: hidden;
		cursor: wait;
		height: var(--height);
